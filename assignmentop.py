#Assignment operator in python i.e '='
#+= ,-=,*=,/=,//=,**=,%= shorthand assignment operator
a=5
print(a)#5
#multiple values
a,b,c=1,2,3
print(a,b,c)#1,2,3
# += shorthand assignment operator
d,e=4,3
f=d+e
d+=2
f+=d
print(d)#6
print(f)#13
# -= shorthand assignment operator
m=2
n=1
m-=n
print(m)#1
# *= shorthand assignment operator
m*=n
print(m)#1
# /=shorthand assignment operator
m/=n
print(m)#1.0
# %= shorthand assignment operator
m%=n
print(m)#0.0
#//= shorthand assignment operator
d//=2
print(d)#3
# **= shorthand assignment operator
d**=2
print(d)#9



